// 키보드 입력 처리
document.addEventListener("keydown", (event: KeyboardEvent) => {
    switch (event.key) {
        case "ArrowUp":
            break;
        case "ArrowDown":
            break;
        case "ArrowLeft":
            break;
        case "ArrowRight":
            break;
    }
});

// 이동 방향 타입
type Direction = "up" | "down" | "left" | "right";
